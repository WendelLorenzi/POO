import java.io.*;

	public class Operad{
		static int a= 5; static int b= 5;

		public static void main(String arg[]){
			
			System.out.println("\n Calculadora básica ");
			int aa= a + b;
			System.out.println("\n Soma A + B: "+ aa);
			int bb= a - b;
			System.out.println("\n Subtração A - B: "+ bb);
			int cc= a/b;
			System.out.println("\n Divisão A/B: "+ cc);
			int dd= a*b;
			System.out.println("\n Multiplicação A*B: "+dd);
		}
	}